class Result
{

    /*
     * Complete the 'luckBalance' function below.
     *
     * The function is expected to return an INTEGER.
     * The function accepts following parameters:
     *  1. INTEGER k
     *  2. 2D_INTEGER_ARRAY contests
     */

    public static int luckBalance(int k, List<List<int>> contests)
    {
        List<int> imp = new List<int>();
        int luck = 0;

        for (int i = 0; i < contests.Count; ++i)
        {
            if (contests[i][1] != 0)
            {
                imp.Add(contests[i][0]);
                luck -= contests[i][0];
            }
            else
            {
                luck += contests[i][0];
            }
        }

        imp.Sort((a, b) => b.CompareTo(a));

        for (int i = 0; i < Math.Min(k, (int)imp.Count); ++i)
        {
            luck += 2 * imp[i];
        }

        return luck;
    }
}

class Solution
{
    public static void Main(string[] args)
    {
        TextWriter textWriter = new StreamWriter(@System.Environment.GetEnvironmentVariable("OUTPUT_PATH"), true);

        string[] firstMultipleInput = Console.ReadLine().TrimEnd().Split(' ');

        int n = Convert.ToInt32(firstMultipleInput[0]);

        int k = Convert.ToInt32(firstMultipleInput[1]);

        List<List<int>> contests = new List<List<int>>();

        for (int i = 0; i < n; i++)
        {
            contests.Add(Console.ReadLine().TrimEnd().Split(' ').ToList().Select(contestsTemp => Convert.ToInt32(contestsTemp)).ToList());
        }

        int result = Result.luckBalance(k, contests);

        textWriter.WriteLine(result);

        textWriter.Flush();
        textWriter.Close();
    }
}
